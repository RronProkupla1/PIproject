	<p style="margin-top: 100px; text-align: center; margin-bottom: 50px;">Face ID review</p>
	<p>
		Face ID is a facial recognition system designed and developed by Apple Inc. 
		for the iPhone and iPad Pro. 
		The system allows biometric
	authentication for unlocking a device, making payments, and accessing sensitive data,
	 as well as providing detailed facial expression
	tracking for Animoji and other features. Initially released in September 2017 
	with the iPhone X, it has since been updated and 
	introduced to all new iPhone and iPad Pro models.</p>

	<p>
		The Face ID hardware consists of a sensor with two modules; 
		one projects a grid of small infrared dots onto a user's face, and 
	another module reads the resulting pattern and generates a 3D facial map. 
	This map is compared with the registered face using a secure
	subsystem, and the user is authenticated if the two faces match sufficiently.
	The system can recognize faces with glasses, clothing,
	makeup, and facial hair, and adapts to changes in appearance over time.</p>

	<p>
		Face ID has sparked a number of debates about security and privacy. 
		Apple claims it is significantly more advanced than Touch ID and
	has far fewer false positives in general, though Face ID has shown mixed
	 results when trying to separate identical twins. Multiple 
	security features largely limit the risk of the system being bypassed using
	 photos or masks, and only one proof-of-concept attempt using
	detailed scans has succeeded. Debate continues over the lack of legal 
	protections offered by biometric systems as compared to passcode
	authentication in the United States. Privacy advocates have also expressed
	 concern about third-party app developers' access to "rough
	maps" of user facial data, despite rigid requirements by Apple of how developers handle facial data.</p>
